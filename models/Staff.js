// ==========================================================
// backend/models/Staff.js
// ==========================================================
// スタッフモデル（CommonJS構成）
// ----------------------------------------------------------
// ✅ bcryptでパスワード暗号化
// ✅ ロール（権限）を定義：admin / manager / store_admin / store_staff / staff
// ✅ 各ロールの権限レベル：顧客情報の参照可否や管理範囲を区別
// ✅ MongoDB Mongoose モデル定義
// ==========================================================

const mongoose = require("mongoose");
const bcrypt = require("bcryptjs");

// ==========================================================
// スタッフスキーマ定義
// ==========================================================
const staffSchema = new mongoose.Schema(
  {
    // ------------------------------------------------------
    // スタッフ名
    // ------------------------------------------------------
    name: {
      type: String,
      required: [true, "名前は必須です"],
      trim: true,
    },

    // ------------------------------------------------------
    // メールアドレス
    // ------------------------------------------------------
    email: {
      type: String,
      required: [true, "メールアドレスは必須です"],
      unique: true,
      lowercase: true,
      match: [/^\S+@\S+\.\S+$/, "有効なメールアドレスを入力してください"],
    },

    // ------------------------------------------------------
    // パスワード（bcryptでハッシュ化される）
    // ------------------------------------------------------
    password: {
      type: String,
      required: [true, "パスワードは必須です"],
      minlength: [6, "パスワードは6文字以上である必要があります"],
    },

    // ------------------------------------------------------
    // ロール（役割）
    // ------------------------------------------------------
    // 顧客一覧取得権限まとめ：
    // admin        → ✅ 全システム管理者（全店舗・全顧客閲覧可）
    // manager      → ✅ 本部マネージャー（全店舗顧客閲覧可）
    // store_admin  → ✅ 店舗管理者（自店舗の顧客閲覧可）
    // store_staff  → ⚠️ 条件付き（自店舗閲覧のみ、追加・削除不可）
    // staff        → ❌ 一般スタッフ（顧客情報アクセス不可）
    role: {
      type: String,
      enum: ["admin", "manager", "store_admin", "store_staff", "staff"],
      default: "staff",
    },

    // ------------------------------------------------------
    // 所属店舗（必要に応じて null 可）
    // ------------------------------------------------------
    store: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Shop",
      default: null,
    },
  },
  {
    timestamps: true, // 作成日時・更新日時を自動付与
  }
);

// ==========================================================
// 保存前処理：パスワードの暗号化
// ----------------------------------------------------------
// - 新規作成またはパスワード更新時のみハッシュ化を実行
// - 既にハッシュ済みの場合はスキップ
// ==========================================================
staffSchema.pre("save", async function (next) {
  if (!this.isModified("password")) return next(); // パスワード未変更ならスキップ

  const salt = await bcrypt.genSalt(10);
  this.password = await bcrypt.hash(this.password, salt);
  next();
});

// ==========================================================
// インスタンスメソッド：パスワード照合
// ----------------------------------------------------------
// ログイン時に入力パスワードをハッシュと比較する
// ==========================================================
staffSchema.methods.matchPassword = async function (enteredPassword) {
  return await bcrypt.compare(enteredPassword, this.password);
};

// ==========================================================
// JSON変換時：パスワード除外
// ----------------------------------------------------------
// APIレスポンスでパスワードを含めないようにする
// ==========================================================
staffSchema.methods.toJSON = function () {
  const obj = this.toObject();
  delete obj.password;
  return obj;
};

// ==========================================================
// モデルエクスポート（CommonJS構成）
// ==========================================================
module.exports = mongoose.model("Staff", staffSchema);
